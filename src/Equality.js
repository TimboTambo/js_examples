console.log("Equality (==)");

console.log("1 == 1");
console.log(1 == 1);
// expected output: true

console.log("'hello' == 'hello'")
console.log('hello' == 'hello');
// expected output: true

console.log("'1' == 1")
console.log('1' ==  1);
// expected output: true

console.log("0 == false")
console.log(0 == false);
// expected output: true


console.log("Strict equality (===)");

console.log("1 === 1")
console.log(1 === 1);
// expected output: true

console.log("'hello' === 'hello'")
console.log('hello' === 'hello');
// expected output: true

console.log("'1' === 1")
console.log('1' ===  1);
// expected output: false

console.log("0 === false")
console.log(0 === false);
// expected output: false




